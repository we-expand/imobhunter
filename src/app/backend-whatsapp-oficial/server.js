app.post('/api/whatsapp/send', async (req, res) => {
  const { to, message, template } = req.body;

  if (!to) {
    return res.status(400).json({ 
      success: false, 
      error: 'NГєmero de destino (to) Г© obrigatГіrio' 
    });
  }

  if (!message && !template) {
    return res.status(400).json({ 
      success: false, 
      error: 'Mensagem ou template Г© obrigatГіrio' 
    });
  }

  try {
    // Formata o nГєmero (remove caracteres nГЈo numГ©ricos)
    const phoneNumber = to.replace(/\D/g, '');

    // Payload para envio de mensagem simples
    const payload = {
      messaging_product: 'whatsapp',
      recipient_type: 'individual',
      to: phoneNumber,
      type: 'text',
      text: {
        preview_url: false,
        body: message
      }
    };

    // Se for template, usa estrutura diferente
    if (template) {
      payload.type = 'template';
      payload.template = {
        name: template.name,
        language: {
          code: template.language || 'pt_BR'
        },
        components: template.components || []
      };
      delete payload.text;
    }

    console.log('рџ“¤ Enviando mensagem WhatsApp:', {
      to: phoneNumber,
      type: template ? 'template' : 'text',
      message: message?.substring(0, 50) || template?.name
    });

    const response = await axios.post(WHATSAPP_API_URL, payload, {
      headers: {
        'Authorization': `Bearer ${WHATSAPP_TOKEN}`,
        'Content-Type': 'application/json'
      }
    });

    console.log('вњ… Mensagem enviada com sucesso!', response.data);

    res.json({
      success: true,
      message_id: response.data.messages[0].id,
      data: response.data
    });

  } catch (error) {
    console.error('вќЊ Erro ao enviar mensagem:', error.response?.data || error.message);
    
    res.status(500).json({
      success: false,
      error: error.response?.data?.error?.message || error.message,
      details: error.response?.data
    });
  }
});

