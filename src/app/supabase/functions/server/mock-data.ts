// Função para filtrar dados mock baseado nos filtros
export function filterMockPeople(filters: any): any[] {
  console.log('🎭 [filterMockPeople] Iniciando filtragem...');
  console.log('🎭 [filterMockPeople] Filtros recebidos:', JSON.stringify(filters, null, 2));
  console.log('🎭 [filterMockPeople] Total de leads mock disponíveis:', mockPeopleData.length);
  
  let results = [...mockPeopleData];
  
  // Verificar se há ALGUM filtro real aplicado
  const hasRealFilters = 
    (filters.keywords && filters.keywords.length > 0) ||
    (filters.currentJobTitle && Array.isArray(filters.currentJobTitle) && filters.currentJobTitle.length > 0) ||
    (filters.seniority && Array.isArray(filters.seniority) && filters.seniority.length > 0) ||
    (filters.city && Array.isArray(filters.city) && filters.city.length > 0) ||
    (filters.currentCompany && Array.isArray(filters.currentCompany) && filters.currentCompany.length > 0) ||
    (filters.hasEmail === true) ||
    (filters.hasPhone === true);
  
  if (!hasRealFilters) {
    console.log('🎭 [filterMockPeople] ✅ NENHUM FILTRO REAL DETECTADO - Retornando TODOS os 12 leads mock');
    return results;
  }
  
  console.log('🎭 [filterMockPeople] ⚠️ Filtros reais detectados - Aplicando filtragem...');
  
  // Filtro por keyword - apenas se existir e não for vazio
  if (filters.keywords && filters.keywords.length > 0) {
    console.log('🎭 [filterMockPeople] Aplicando filtro de keywords:', filters.keywords);
    const keywords = Array.isArray(filters.keywords) ? filters.keywords : [filters.keywords];
    results = results.filter(person => {
      const searchText = `${person.name} ${person.title} ${person.company} ${person.headline}`.toLowerCase();
      return keywords.some((kw: string) => searchText.includes(kw.toLowerCase()));
    });
    console.log('🎭 [filterMockPeople] Após filtro de keywords:', results.length);
  }
  
  // Filtro por cargo - apenas se existir e não for vazio
  if (filters.currentJobTitle && Array.isArray(filters.currentJobTitle) && filters.currentJobTitle.length > 0) {
    console.log('🎭 [filterMockPeople] Aplicando filtro de cargo:', filters.currentJobTitle);
    results = results.filter(person => 
      filters.currentJobTitle.some((title: string) => 
        person.title.toLowerCase().includes(title.toLowerCase())
      )
    );
    console.log('🎭 [filterMockPeople] Após filtro de cargo:', results.length);
  }
  
  // Filtro por seniority - apenas se existir e não for vazio
  if (filters.seniority && Array.isArray(filters.seniority) && filters.seniority.length > 0) {
    console.log('🎭 [filterMockPeople] Aplicando filtro de seniority:', filters.seniority);
    results = results.filter(person => 
      filters.seniority.some((level: string) => 
        person.seniority && person.seniority.toLowerCase().includes(level.toLowerCase())
      )
    );
    console.log('🎭 [filterMockPeople] Após filtro de seniority:', results.length);
  }
  
  // Filtro por localização - apenas se existir e não for vazio
  if (filters.city && Array.isArray(filters.city) && filters.city.length > 0) {
    console.log('🎭 [filterMockPeople] Aplicando filtro de cidade:', filters.city);
    results = results.filter(person =>
      filters.city.some((city: string) =>
        person.location.toLowerCase().includes(city.toLowerCase())
      )
    );
    console.log('🎭 [filterMockPeople] Após filtro de cidade:', results.length);
  }
  
  // Filtro por empresa - apenas se existir e não for vazio
  if (filters.currentCompany && Array.isArray(filters.currentCompany) && filters.currentCompany.length > 0) {
    console.log('🎭 [filterMockPeople] Aplicando filtro de empresa:', filters.currentCompany);
    results = results.filter(person =>
      filters.currentCompany.some((company: string) =>
        person.company.toLowerCase().includes(company.toLowerCase())
      )
    );
    console.log('🎭 [filterMockPeople] Após filtro de empresa:', results.length);
  }
  
  // Filtro por ter email
  if (filters.hasEmail === true) {
    console.log('🎭 [filterMockPeople] Aplicando filtro de email obrigatório');
    results = results.filter(person => !!person.email);
    console.log('🎭 [filterMockPeople] Após filtro de email:', results.length);
  }
  
  // Filtro por ter telefone
  if (filters.hasPhone === true) {
    console.log('🎭 [filterMockPeople] Aplicando filtro de telefone obrigatório');
    results = results.filter(person => !!person.phone);
    console.log('🎭 [filterMockPeople] Após filtro de telefone:', results.length);
  }
  
  console.log('🎭 [filterMockPeople] Total de resultados após filtragem:', results.length);
  return results;
}

export function filterMockCompanies(filters: any): any[] {
  let results = [...mockCompaniesData];
  
  // Filtro por indústria
  if (filters.industry && filters.industry.length > 0) {
    results = results.filter(company =>
      filters.industry.some((ind: string) =>
        company.industry.toLowerCase().includes(ind.toLowerCase())
      )
    );
  }
  
  // Filtro por tamanho
  if (filters.companySize && filters.companySize.length > 0) {
    results = results.filter(company =>
      filters.companySize.includes(company.size)
    );
  }
  
  // Filtro por localização
  if (filters.city && filters.city.length > 0) {
    results = results.filter(company =>
      filters.city.some((city: string) =>
        company.location.toLowerCase().includes(city.toLowerCase())
      )
    );
  }
  
  return results;
}